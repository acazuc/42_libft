#include "libft.h"

long	ft_swap_long(long l)
{
	return (((l >> 56) & 0xff) | ((l >> 40) & 0xff00) | ((l >> 24) & 0xff0000)
			| ((l >> 8) & 0xff000000) | ((l & 0xff000000) << 8)
			| ((l & 0xff0000) << 24) | ((l & 0xff00) << 40)
			| ((l & 0xff) << 56));
}
