#include "libft.h"

void	ft_memor(void *dst, void *v1, void *v2, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		((uint8_t*)dst)[i] = ((uint8_t*)v1)[i] | ((uint8_t*)v2)[i];
		++i;
	}
}
